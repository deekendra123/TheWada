package wada.programmics.thewada.ObjectClass;

public class DiscountCardData {

    private String name;
    private int images_url;

    public DiscountCardData() {
        this.name = name;
        this.images_url = images_url;
    }

    public String getName() {
        return name;
    }

    public int getImages_url() {
        return images_url;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setImages_url(int images_url) {
        this.images_url = images_url;
    }
}
